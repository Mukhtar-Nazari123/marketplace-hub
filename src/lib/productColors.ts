export type ProductColorValue = typeof PRODUCT_COLORS[number]['value'];

export interface ProductColor {
  value: string;
  name: string;
  nameFa: string;
  namePs: string;
  hex: string;
}

/**
 * Get color definition by value
 */
export const getColorByValue = (value: string): ProductColor | undefined => {
  return PRODUCT_COLORS.find(c => c.value === value);
};

/**
 * Get localized color name with trilingual support
 */
export const getLocalizedColorName = (value: string, language: string): string => {
  const color = getColorByValue(value);
  if (!color) return value;
  if (language === 'ps') return color.namePs || color.nameFa;
  if (language === 'fa') return color.nameFa;
  return color.name;
};
